public class primitivedt{
    public static void main(String args[]){
        byte b = 8;
        System.out.println(b);
        char ch = 'a';//only single character stored and datatype char 'c' always in small
        System.out.println(ch);
        boolean var = false;// this datatype used for condition check
        System.out.println(var);
        int a = 12;// you can store the value upto 4 bytes means 32 bits
        System.out.println(a);
        long c = 1345626216;// you can store the value upto 8 bytes means 8*8=64 bits
        System.out.println(c);
        float f = 1123.12f;// here you have to tell to its float value because java consider automatically its double value
        System.out.println(f);
        double d = 12.123;
        System.out.println(d);
    }
    
}
